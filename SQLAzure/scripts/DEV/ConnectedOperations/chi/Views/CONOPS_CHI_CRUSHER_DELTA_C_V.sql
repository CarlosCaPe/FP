CREATE VIEW [CHI].[CONOPS_CHI_CRUSHER_DELTA_C_V]
AS

SELECT
	dc.SITE_CODE,
	s.SHIFTFLAG,
	dc.SHIFTINDEX,
	CASE dc.DUMPNAME
		WHEN 'CRUSHER' THEN 'CRUSHER'
	END AS CRUSHER,
	AVG(dc.DELTA_C) AS DELTA_C
FROM CHI.CONOPS_CHI_SHIFT_INFO_V s
LEFT JOIN dbo.delta_c dc WITH(NOLOCK)
	ON s.shiftindex = dc.shiftindex
WHERE SITE_CODE = 'CHI'
	AND UNIT = 'Crusher'
GROUP BY dc.SITE_CODE, dc.SHIFTINDEX, s.SHIFTFLAG, dc.DUMPNAME

