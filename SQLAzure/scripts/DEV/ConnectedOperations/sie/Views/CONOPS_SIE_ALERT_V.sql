CREATE VIEW [SIE].[CONOPS_SIE_ALERT_V]
AS

	SELECT si.SHIFTFLAG
		  ,si.[SITEFLAG]
		  ,[EQMT_TYPE]
		  ,[EQMTID]
		  ,[OPERATOR_ID]
		  ,[ALERT_TYPE]
		  ,[ALERT_NAME]
		  ,[ALERT_DESCRIPTION]
		  ,[ALERT_DATE]
		  ,[DURATION]
		  ,[ALERT_GENERATED_DATETIME]
		  ,[UTC_CREATED_DATE]
	FROM [SIE].[CONOPS_SIE_SHIFT_INFO_V] si (NOLOCK)
	LEFT JOIN [SIE].[ALERT] a (NOLOCK)
	ON a.SHIFTINDEX = si.SHIFTINDEX
	   AND a.SITEFLAG = si.SITEFLAG

