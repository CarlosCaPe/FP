CREATE VIEW [SIE].[CONOPS_SIE_CRUSHER_DELTA_C_V]
AS

SELECT
	dc.SITE_CODE,
	s.SHIFTFLAG,
	dc.SHIFTINDEX,
	CASE dc.DUMPNAME
		WHEN 'A-SIDE' THEN 'A-SIDE'
		WHEN 'B-SIDE' THEN 'B-SIDE'
	END AS CRUSHER,
	AVG(dc.DELTA_C) AS DELTA_C
FROM SIE.CONOPS_SIE_SHIFT_INFO_V s
LEFT JOIN dbo.delta_c dc WITH(NOLOCK)
	ON s.shiftindex = dc.shiftindex
WHERE SITE_CODE = 'SIE'
	AND UNIT = 'Crusher'
GROUP BY dc.SITE_CODE, dc.SHIFTINDEX, s.SHIFTFLAG, dc.DUMPNAME


