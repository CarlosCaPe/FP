CREATE VIEW [bag].[CONOPS_BAG_CM_HOURLY_INTERMEDIATE_STOCKPILE_V]
AS    

WITH CTE AS (
SELECT 
	shiftindex,
	CRUSHERLOC,
	COMPONENT,
	SENSORVALUE,
	CASE WHEN DATEPART(MINUTE,VALUE_TS) BETWEEN 0 AND 5 THEN VALUE_TS END AS TimeInHour
FROM [dbo].[IOS_STOCKPILE_LEVELS] WITH (NOLOCK)
WHERE SITEFLAG = 'BAG'
),

MFLStockpile AS(
SELECT
	SHIFTINDEX,
	SENSORVALUE AS MFLStockpile,
	TimeInHour
FROM CTE
WHERE CRUSHERLOC = '' 
	AND COMPONENT = ''
),

MillStockpile AS(
SELECT
	SHIFTINDEX,
	SENSORVALUE / 165000 * 100 AS MillStockpile,
	TimeInHour
FROM CTE
WHERE CRUSHERLOC = 'Mill Stockpile' 
	AND COMPONENT = 'Mill Stockpile'
)


SELECT 
a.[SITEFLAG]
,a.[SHIFTFLAG]
,0 AS [MFLStockpile]
,m.[MillStockpile]
,CAST(CONCAT(FORMAT([m].TimeInHour, 'yyyy-MM-dd HH'), ':00:00') AS Datetime) AS TimeInHour
FROM [BAG].[CONOPS_BAG_SHIFT_INFO_V] a WITH (NOLOCK)
LEFT JOIN MillStockpile [m] WITH (NOLOCK) 
	ON a.SHIFTINDEX = [m].SHIFTINDEX 
WHERE m.TimeInHour IS NOT NULL

  




